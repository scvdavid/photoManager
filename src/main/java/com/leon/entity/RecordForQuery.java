package com.leon.entity;

public class RecordForQuery {
	private String recordName;
	private int year;
	private int yearB;
	private int yearE;
	


	public String getRecordName() {
		return recordName;
	}

	public void setRecordName(String recordName) {
		this.recordName = recordName;
	}

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}

	public int getYearB() {
		return yearB;
	}

	public void setYearB(int yearB) {
		this.yearB = yearB;
	}

	public int getYearE() {
		return yearE;
	}

	public void setYearE(int yearE) {
		this.yearE = yearE;
	}
	
	
}
